char	*ft_strchr(const char *s, int c)
{
	unsigned char	chr;
	int				i;

	chr = (unsigned char)c;
	i = 0;
	while (s[i] != chr && s[i] != '\0')
		i++;
	if (s[i] != chr)
		return (0);
	return ((char *)s + i);
}
